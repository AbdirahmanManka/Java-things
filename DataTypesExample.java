public class DataTypesExample {
    public static void main(String[] args) {
        // Primitive data types
        byte myByte = 100; // Example of a byte
        short myShort = 5000; // Example of a short
        int myInt = 100000; // Example of an int
        long myLong = 15000000000L; // Example of a long
        float myFloat = 5.75f; // Example of a float
        double myDouble = 19.99; // Example of a double
        boolean myBoolean = true; // Example of a boolean
        char myChar = 'A'; // Example of a char

        // Reference data types
        String myString = "Hello World"; // Example of a string

        // Printing all the variables
        System.out.println("Byte value: " + myByte);
        System.out.println("Short value: " + myShort);
        System.out.println("Int value: " + myInt);
        System.out.println("Long value: " + myLong);
        System.out.println("Float value: " + myFloat);
        System.out.println("Double value: " + myDouble);
        System.out.println("Boolean value: " + myBoolean);
        System.out.println("Char value: " + myChar);
        System.out.println("String value: " + myString);
    }
}
